# Введення даних: користувач пише числа через пробіл (наприклад: 5 1 9 3)
raw_input = input("Введіть числа через пробіл: ")

# Перетворюємо введений текст на список чисел
arr = [int(x) for x in raw_input.split()]
n = len(arr)

# Алгоритм сортування бульбашкою
for i in range(n - 1):
    for j in range(n - 1 - i):
        if arr[j] > arr[j + 1]:
            # Міняємо елементи місцями
            arr[j], arr[j + 1] = arr[j + 1], arr[j]

print("Відсортований масив:", arr)